# Задача-4: Дан список заполненный произвольными целыми числами
# Получите новый список, элементами которого будут только уникальные элементы исходного

print('УНИКАЛЬНЫЕ ЭЛЕМЕНТЫ:\n' + '-' * 10)

a = [int(randint(1, 20)) for i in range(randint(5, 15))]
b = []
if len(a) == 1:
    b.append(a[0])
for i in range(len(a)):
    if a.count(a[i]) == 1:
        b.append(a[i])

print('Начальный список: ', a, '\nКонечный список:  ', b, '\n' + '-' * 10, sep='')
