# Задача-1:
# Дан список заполненный произвольными целыми числами, получите новый список элементами
# которого будут квадратные корни элементов исходного списка, но только если результаты
# извлечения корня не имеют десятичной части и если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

print('СПИСОК:\n' + '-' * 10)

from math import sqrt

a = [2, -5, 8, 9, -25, 25, 4, 16, 64, 3]
b = []
for i in a:
    if i > 0:
        if (sqrt(i) % 1) == 0:
            b.append(int(sqrt(i)))
print('Начальный список:', a, '\n' + 'Конечный список:', b, '\n' + '-' * 10 + '\n')

